public class Pattern1 {
    public static void main(String[] args)
    {
        int n=5;
        int m=5,i=1;
        while(i<=n) {
            int j=1;
            while(j<=m)
            {
                System.out.print(j);
                j++;
            }
            System.out.println(" ");
            i++;

        }





    }
}
